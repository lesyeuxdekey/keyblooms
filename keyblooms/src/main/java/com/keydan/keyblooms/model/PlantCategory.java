package com.keydan.keyblooms.model;

public enum PlantCategory {
 FOLIAGE_INTERIOR, // For plants with striking leaves like Monstera, Pothos, Ficus
    TRAILING,         // For climbing or hanging plants like Spider Plants or Inch Plants (Transilvania)
    FLOWERING,        // For plants where the blooms stand out, like Orchids
    FERN,             // Specific category for ferns due to their high humidity needs
    SUCCULENT         // For future additions like cacti or fleshy plants
}
